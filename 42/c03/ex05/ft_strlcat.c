#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

unsigned int	ft_strlcat(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	int				j;
	int				l;

	j = 0;
	i = ft_strlen(dest);
	if (size == 0 || size < i)
		return (ft_strlen(src) + size);
	while (src[j] && i + j < size - 1)
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	l = ft_strlen(dest) + ft_strlen(src);
	return (l);
}
